## *Classes
## *Objetos
## *Encapsulamento
## *Heranças, dentre outros.

Um pouco sobre a historia de Alan Kay e POO
#O termo Programação orientada a objetos (POO) foi criado por Alan Kay no final da decada de 1960, autor da linguagem de programação SmallTalk, no entando, somente na década de 1990, o modelo passou a ser adotado por grandes empresas e desenvolvedores de software do mundo

#De uma forma mais geral, Alan Kay pensou em como construir um sistema de software a partir de agentes autônomos que interagem entre si, como se fossem células

#Alan kay definiu os fundamentos da OO (Orientação a objetos) através da analogia biologica, os fundamentos são baseados nos tópicos:
1- Qualquer coisa é um objeto
2- Objetos realizam tarefas através da requisição de serviços a outros objetos
3- Cada objeto pertence a uma determinada classe
4- Uma classe agrupa obejtos similares
5- A Classe é um repositório para comportamento associado ao objeto
6- Classes são organizados por hierarquias

#Os pilares da OO(Orientação a objetos):
1- Abstração
2- Encapsulamento
3- Herança
4- Polimorfismo

Abstração(classes): Imaginar o objeto que tenha: Identidade unica, Atributos, Métodos
Encapsulamento(attr): Esconder as propriedades dos objetos para obter mais segurança
Herança(herdar uma classe): Permitir o reuso de código
Polimorfismo(referenciar um objeto de varias formas diferentes): Capacidade de referenciar um objeto de varias formas diferentes


