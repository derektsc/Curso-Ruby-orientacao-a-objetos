##Podemos dizer que POO (programação orientada a objetos) é um paradigma para desenvolvimento de software que baseia-se na utilização de componentes individuais (objetos) que colaboram para construir sistemas mais complexos, a colaboração entre os objetos é feita através do envio de mensagens

