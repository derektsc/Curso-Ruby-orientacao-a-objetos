=begin
    Módulos ruby são similares a classes em relação ao fato de que também armazenam uma coleção de métoodos, constantes, outras definições de módulos e classes.

    Módulos ruby são diferente das classes em relação ao fato de não poder criar objetos baseados em módulos nem pode criar módulos que herda esse módulo.

    Módulo é um bom lugar para armazenar constantes em um local centralizado.

    Objetivos: Qual o objetivo de criarmos um módulo, primeiro eles agem como namespace, permitindo que voce defina os métodos cujos nomes não irão colidir com aqueles definidos em outras partes de um programa. Em segundo lugar, permitem que você compartilhe funcionalidade entre classes.
=end

