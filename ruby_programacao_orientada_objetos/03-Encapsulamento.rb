O que é Encapsulamento?

Se refere a esconder os dados (atributos/métodos) dentro do objeto;
    *produz duas visões do objeto:
        1- Visão interna (Como o objeto faz)
        2- Visão Externa (O que o objeto faz)
    end

Exemplo:
O funcionamento interno de um telefone é escondido (ou encapsulado) do usuário. Para o usuário realizar uma chamada telefônica (visão externa) ele não precisa conhecer os detalhes da eletrônica utilizados no telefone (visão interna)

Se tratando de classes, o encapsulamento é obtido através da visibilidade, conhecida também como "modificadores de acesso".

Os modificadores de acessos mais comuns são:

1- public / público: Representado por um sinal de mais "+" na notação UML. Elementos declarados como público podem ser acessados a partir de qualquer outra classe;

2- Private / privado: Representado por um sinal de menos "-" na notação UML. Elementos private só podem ser acessados pela própria classe;

3- Protected / protegido: Representado por um sinal de sustenido "#" na notação UML. Elementos protected só podem ser acessados pela própria classe e por suas descendentes

